DROP TABLE RED;
CREATE TABLE RED(
	NO NUMBER PRIMARY KEY,
	ID VARCHAR2(20) NOT NULL UNIQUE,
	PW VARCHAR2(20) NOT NULL,
	NAME VARCHAR2(20),
	AGE NUMBER,
	EMAIL VARCHAR2(100) NOT NULL UNIQUE,
	REGDATE DATE
);

DROP SEQUENCE RED_SEQ;

CREATE SEQUENCE RED_SEQ
START WITH 1
INCREMENT BY 1
MAXVALUE 999999
NOCYCLE 
NOCACHE;